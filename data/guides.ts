export function getGuideBySlug(slug: string) {
  return guides.find((guide) => guide.slug === slug);
}

export function getGuidesBySlugs(slugs: string[]) {
  return slugs
    .map((slug) => getGuideBySlug(slug))
    .filter((guide): guide is Guide => Boolean(guide));
}

export function getGuidesForProduct(productSlug: string, collectionPaths: string[] = []) {
  return guides.filter(
    (guide) =>
      guide.relatedProducts.includes(productSlug) ||
      guide.relatedCollections.some((collection) => collectionPaths.includes(collection.path)),
  );
}
